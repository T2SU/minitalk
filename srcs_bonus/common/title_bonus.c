static void	print_client(void)
{
	ft_putstr("       ____ _ _            _   \n");
	ft_putstr("      / ___| (_) ___ _ __ | |_ \n");
	ft_putstr("     | |   | | |/ _ \\ '_ \\| __|\n");
	ft_putstr("     | |___| | |  __/ | | | |_ \n");
	ft_putstr("      \\____|_|_|\\___|_| |_|\\__|\n");
	ft_putstr("                               \n");
}

static void	print_server(void)
{
	ft_putstr("      ____                           \n");
	ft_putstr("     / ___|  ___ _ ____   _____ _ __ \n");
	ft_putstr("     \\___ \\ / _ \\ '__\\ \\ / / _ \\ '__|\n");
	ft_putstr("      ___) |  __/ |   \\ V /  __/ |   \n");
	ft_putstr("     |____/ \\___|_|    \\_/ \\___|_|   \n");
	ft_putstr("                                     \n");
}

void	print_title(int mode)
{
	print_minitalk();
	if (mode == kServer)
		print_server();
	if (mode == kClient)
		print_client();
}
